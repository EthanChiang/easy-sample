package com.imooc;

public class Telephone {
	private float screen;
	private float cpu;
	public Telephone(){
		System.out.println("這是第一個Telephone");
	}
	public float getScreen() {
		return screen;
	}
	public void setScreen(float screen) {
		this.screen = screen;
	}
	public float getCpu() {
		return cpu;
	}
	public void setCpu(float cpu) {
		this.cpu = cpu;
	}
	
	
}
